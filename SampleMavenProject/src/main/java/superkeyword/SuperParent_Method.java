package superkeyword;

public class SuperParent_Method {
	
	public void display() { // non parameterized method
		
		System.out.println("this is from parent class method display()");
	}
	
	public String color(String col) {
		return col;
	}
	
	public void show() {
		System.out.println("this is from parent class show() method");

	}
}
